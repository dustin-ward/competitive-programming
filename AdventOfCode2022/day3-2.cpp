int main() {
	string s;
	int ans = 0;
	while(getline(cin,s)) {
		string s2,s3;
		getline(cin,s2);
		getline(cin,s3);
		char ansC;
		for(char c:s) {
			if(s2.find(c) != -1 && s3.find(c) != -1) {
				ansC = c;
				break;
			}
		}
		
		if(ansC < 'a') {
			ans += ansC-'A'+27;
		}
		else
			ans += ansC-'a'+1;
	}
	cout<<ans<<endl;
}
