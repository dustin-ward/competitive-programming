int main() {
	string s;
	int ans = 0;
	while(getline(cin,s)) {
		set<char> S;
		string l,r;
		l = s.substr(0,sz(s)/2);
		r = s.substr(sz(s)/2);
		for(char c:l) {
			if(r.find(c) != -1) {
				if(S.count(c)) continue;
				S.insert(c);
				if(c < 'a') {
					ans += c-'A'+27;
				}
				else
					ans += c-'a'+1;
			}
		}
	}
	cout<<ans<<endl;
}
