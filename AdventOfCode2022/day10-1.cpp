set<int> C = {20, 60, 100, 140, 180, 220};
int ans = 0;

void checkAns(int c, int X) {
    if(C.count(c))
        ans += c*X;
}

int main() {
    string s;
    int c = 1;
    int X = 1;
    while(getline(cin,s)) {
        if(s == "noop") {
            c++;
            checkAns(c,X);
        }
        else {
            istringstream iss(s);
            string inst;
            int A;
            iss>>inst>>A;
            assert(inst == "addx");
            c++;
            checkAns(c,X);
            c++;
            X+=A;
            checkAns(c,X);
        }
    }
    cout<<ans<<endl;
}
