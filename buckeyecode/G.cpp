int ans = INT_MAX;
ll dis;
int N;
vector<vector<pair<int,ll>>> G;
vi V;

void dfs(int n, ll curD) {
	if(n == 1) {
		if(curD % 256 < ans || (curD % 256 == ans && curD < dis)) {
			ans = curD % 256;
			dis = curD;
		}
		return;
	}
	for(auto &p:G[n]) {
		V[p.fst] = 1;
		dfs(p.fst, curD+p.snd);
		V[p.fst] = 0;
	}
}

int main() {
	cin>>N;
	G.resize(1001);
	V.resize(1001, 0);

	for(int i=0; i<N; ++i) {
		int S,T,L; cin>>S>>T>>L;
		G[S-1].push_back({T-1,L});
	}

	dfs(0, 0);

	cout<<ans<<" "<<dis<<endl;
}
