int main() {
	string fav; cin>>fav;
	int N; cin>>N;

	map<string,int> M;
	for(int i=0; i<N; ++i) {
		string s; cin>>s;
		M[s]++;
	}

	string most;
	int count = 0;
	for(auto &p:M) {
		if(p.snd > count || (p.snd == count && p.fst == fav)) {
			count = p.snd;
			most = p.fst;
		}
	}

	cout<<(most == fav ? "Don't trade" : "Trade")<<endl;
}
