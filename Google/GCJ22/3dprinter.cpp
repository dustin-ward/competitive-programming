int main() {
    int T,caseno=1; cin>>T;
    while(T--) {
        vi C(3),M(3),Y(3),K(3);
        int c=0,m=0,y=0,k=0;

        for(int i=0; i<3; ++i)
            cin>>C[i]>>M[i]>>Y[i]>>K[i];

        sort(all(C));
        sort(all(M));
        sort(all(Y));
        sort(all(K));

        int sum = 0;
        c = C[0];
        sum += c;

        if(sum + M[0] > 1000000)
            m = 1000000 - sum;
        else
            m = M[0];
        sum += m;

        if(sum + Y[0] > 1000000)
            y = 1000000 - sum;
        else
            y = Y[0];
        sum += y;
        
        if(sum + K[0] > 1000000)
            k = 1000000 - sum;
        else
            k = K[0];
        sum += k;

        if(sum == 1000000)
            printf("Case #%d: %d %d %d %d\n", caseno++, c, m, y, k);
        else
            printf("Case #%d: IMPOSSIBLE\n", caseno++);
    }
}