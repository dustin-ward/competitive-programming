int main() {
    int T,caseno=1; cin>>T;
    while(T--) {
        int R,C; cin>>R>>C;
        vector<vector<char>> V((R*2)+1, vector<char>((2*C)+1, '.'));
        for(int r=0; r<(2*R)+1; ++r) {
            for(int c=0; c<(2*C)+1; ++c) {
                if(!(r%2) && !(c%2))
                    V[r][c] = '+';
                else if(!(r%2))
                    V[r][c] = '-';
                else if(!(c%2))
                    V[r][c] = '|';
            }
        }
        V[0][0] = V[1][0] = V[0][1] = '.';
        cout<<"Case #"<<caseno++<<": \n";
        for(auto v:V) {
            for(auto c:v)
                cout<<c;
            cout<<endl;
        }
    }
}