int main() {
    int T, caseno=1; cin>>T;
    while(T--) {
        string I,P; cin>>I>>P;
        int i=0,p=0;
        int ans=0;
        while(i < sz(I) && p < sz(P)) {
            if(P[p] != I[i]) {
                ans++;
                p++;
            }
            else {
                p++;
                i++;
            }
        }
        ans += sz(P) - p;
        cout<<"Case #"<<caseno++<<": ";
        if(p >= sz(P) && i < sz(I))
            cout<<"IMPOSSIBLE"<<endl;
        else
            cout<<ans<<endl;
    }
}