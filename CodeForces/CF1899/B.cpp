int main() {
    int T; cin>>T;
    while(T--) {
        int N; cin>>N;
        vector<ll> A(N);
        for(ll &i:A)
            cin>>i;

        ll ans = 0;
        for(int k=1; k<N; k++) {
            if(N%k) continue;
            /* debug(k); */

            ll mx=0,mn=INT_MAX;
            ll sum = 0;
            int i = 0;
            while(i<=N) {
                if(i && i%k == 0) {
                    /* debug(i); */
                    /* debug(sum); */
                    mx = max(mx, sum);
                    mn = min(mn, sum);
                    sum = 0;
                }
                if(i==N) break;
                sum += A[i++];
            }
            ans = max(ans, abs(mx-mn));
        }
        cout<<ans<<endl;
    }
}
