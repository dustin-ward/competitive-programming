int main() {
    int T; cin>>T;
    while(T--) {
        int N; cin>>N;
        vector<ll> A(N);
        for(ll &i:A)
            cin>>i;

        int c = 0;
        vi P(N);
        P[0] = c;
        for(int i=1; i<N; i++) {
            if(abs(A[i])%2 == abs(A[i-1])%2)
                c++;
            P[i] = c;
        }
        debug(P);
        
        ll ans = 0;
        int i = 0;
        int last = 0;
        ll sum = 0;
        while(i<N) {
            if(P[i]!=last) {
                ans = max(ans, sum);
                sum = 0;
                last = P[i];
            }
            sum += A[i];
            i++;
        }
        ans = max(ans,sum);
        cout<<ans<<endl;
    }
}
