int main() {
	int T; cin>>T;
	while(T--) {
		int N;
		double D,H; cin>>N>>D>>H;
		double W = (D*H)/2;
		double ans = 0.0;
		int lastY;
		for(int i=0; i<N; i++) {
			int y; cin>>y;
			ans += W;
			if(i == 0) {
				lastY = y;
				continue;
			}

			int v = y - lastY;
			if(v < H) {
				double H2 = H-v;
				double D2 = D * (H2/H);
				ans -= (D2*H2)/2;
			}
			lastY = y;
		}
		cout<<fixed<<setprecision(10)<<ans<<endl;
	}
}
