int main() {
    int T; cin>>T;
    while(T--) {
        string s1,s2; cin>>s1>>s2;
        if(s1[0]==s2[0]) {
            cout<<"YES\n"<<s1[0]<<"*"<<endl;
            continue;
        }
        if(s1[sz(s1)-1]==s2[sz(s2)-1]) {
            cout<<"YES\n"<<"*"<<s1[sz(s1)-1]<<endl;
            continue;
        }

        bool flag = false;
        for(int i=0; i<sz(s1)-1; i++) {
            if(s2.find(s1.substr(i,2))!=-1) {
                cout<<"YES\n"<<"*"<<s1.substr(i,2)<<"*"<<endl;
                flag = true;
                break;
            }
        }
        if(!flag)
            cout<<"NO"<<endl;
    }
}
