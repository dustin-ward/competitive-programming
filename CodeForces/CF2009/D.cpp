int main() {
    int T; cin>>T;
    while(T--) {
        int N; cin>>N;
        set<pii> points;
        unordered_map<int,int> m;
        for(int i=0; i<N; i++) {
            int x,y;
            cin>>x>>y;
            points.emplace(x,y);
            m[x]++;
        }

        ll ans = 0;

        // Up-Down right angle
        for(auto &[_,c] : m) {
            if(c==2) ans += N-2;
        }

        // Turned
        for(auto &point : points) {
            pii p2 = {point.fst-1, (point.snd+1)%2};
            pii p3 = {point.fst+1, (point.snd+1)%2};

            if(points.count(p2) && points.count(p3)) ans++;
        }

        cout<<ans<<endl;
    }
}
