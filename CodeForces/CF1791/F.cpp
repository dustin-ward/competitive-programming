int f(int n) {
    int sum=0;
    while(n) {
        sum+=n%10;
        n/=10;
    }
    return sum;
}

int main() {
    int T; cin>>T;
    while(T--) {
        int N,Q; cin>>N>>Q;
        vi A(N);
        set<int> S;
        for(int i=0; i<N; ++i) {
            cin>>A[i];
            if(A[i]>9) S.insert(i);
        }

        while(Q--) {
            int op,l,r;
            cin>>op>>l;
            l--;
            if(op==1) {
                cin>>r;
                r--;
                while(!S.empty()) {
                    auto it = S.lower_bound(l);
                    if(it == S.end() || *it > r) break;
                    A[*it] = f(A[*it]);
                    l = *it;
                    S.erase(it);
                    if(A[l] > 9) S.insert(l);
                    l++;
                }
            } else {
                cout<<A[l]<<"\n";
            }
        }
    }
}
