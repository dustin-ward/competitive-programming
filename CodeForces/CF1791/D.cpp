int main() {
    int T; cin>>T;
    while(T--) {
        int N; cin>>N;
        string s; cin>>s;

        bool alph[26];
        for(int i=0; i<26; ++i)
            alph[i]=0;

        vector<int> inc(N,0), dec(N,0);
        inc[0] = dec[N-1] = 1;
        alph[s[0]-'a'] = 1;
        for(int i=1; i<N; ++i) {
            inc[i] = inc[i-1];
            if(!alph[s[i]-'a']) {
                inc[i]++;
                alph[s[i]-'a']=1;
            }
        }

        for(int i=0; i<26; ++i)
            alph[i]=0;
        alph[s[N-1]-'a'] = 1;
        for(int i=N-2; i>=0; --i) {
            dec[i] = dec[i+1];
            if(!alph[s[i]-'a']) {
                dec[i]++;
                alph[s[i]-'a']=1;
            }
        }

        int ans = 0;
        for(int i=0; i<N-1; ++i)
            ans = max(ans,inc[i]+dec[i+1]);

        cout<<ans<<endl;
    }
}
