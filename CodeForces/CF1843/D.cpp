int N;
vector<vi> G;
vector<ll> S;

ll sizeOfTree(int n, int p) {
	ll sum = 0;
	if(sz(G[n])==1 && G[n][0]==p) sum = 1;
	else
		for(int &i:G[n])
			if(i!=p)
				sum += sizeOfTree(i,n);
	S[n] = sum;
	return sum;
}

int main() {
	int T; cin>>T;
	while(T--) {
		cin>>N;
		G.clear();
		G.resize(N);
		S.clear();
		S.resize(N);

		for(int i=0; i<N-1; i++) {
			int u,v; cin>>u>>v;
			u--; v--;
			G[u].push_back(v);
			G[v].push_back(u);
		}

		debug(sizeOfTree(0,-1));
		debug(S);

		int Q; cin>>Q;
		for(int i=0; i<Q; i++) {
			int x,y; cin>>x>>y;
			x--; y--;
			cout<<S[x]*S[y]<<endl;
		}
	}
}
