ll N,C;
vector<ll> S;

bool f(ll gap) {
	ll sum = 0;
	for(ll &s:S) {
		ll x = s + (2LL*gap);
		sum += x*x;
		if(sum > C) return true;
	}
	return sum > C;
}

int main() {
	int T; cin>>T;
	while(T--) {
		cin>>N>>C;
		S.clear();
		S.resize(N);
		for(ll &i:S)
			cin>>i;

		ll lo=0;
		ll hi=1E10;
		while(hi-lo>1) {
			ll mid = (lo+hi)/2LL;
			if(f(mid))
				hi = mid;
			else
				lo = mid;
		}
		cout<<lo<<endl;
	}
}
