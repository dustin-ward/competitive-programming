int main() {
	int T; cin>>T;
	while(T--) {
		int N,K; cin>>N>>K;
		vector<ll> A(N), psum(N+1);
		for(ll &i:A)
			cin>>i;

		sort(all(A));
		psum[0] = 0;
		for(int i=1; i<=N; i++)
			psum[i] = A[i-1] + psum[i-1];
		// debug(A);
		// debug(psum);

		ll ans = 0;
		for(int m=0; m<=K; m++) {
			ll sum = psum[(N)-(K-m)] - psum[2*m];
			// debug(m);
			// debug(sum);
			ans = max(ans, sum);
		}
		cout<<ans<<endl;
	}
}
