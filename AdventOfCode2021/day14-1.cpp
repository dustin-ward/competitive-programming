int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    string line;
    string S;
    getline(cin, S);
    getline(cin, line);

    map<string,char> M;
    while(getline(cin, line)) {
        string s1;
        char c;
        istringstream iss(line);
        iss>>s1>>c>>c>>c;
        M.insert(make_pair(s1, c));
    }

    for(int step=0; step<10; step++) {
        for(int i=0; i<S.length(); i++) {
            string s1 = S.substr(i,2);
            if(M.find(s1) != M.end()) {
                S.insert(S.begin()+i+1, M[s1]);
                i++;
            }
        }
        // cout<<step<<" "<<S<<endl;
    }

    map<char,int> freq;
    for(auto c:S)
        freq[c]++;

    int maxE = 0;
    int minE = INT_MAX;
    for(auto p:freq) {
        maxE = max(maxE, p.second);
        minE = min(minE, p.second);
    }
    debug(maxE);
    debug(minE);
    cout<<maxE-minE<<endl;
}