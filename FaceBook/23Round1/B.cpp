vi primeFactors(int n) { 
    vi temp;
    while (n % 2 == 0) { 
        temp.push_back(2);
        n = n/2; 
    } 
 
    for (int i = 3; i <= sqrt(n); i = i + 2) { 
        while (n % i == 0) { 
            temp.push_back(i);
            n = n/i; 
        } 
    } 
 
    if (n > 2) 
        temp.push_back(n);
    return temp;
} 

int main() {
    int T,caseno=1; cin>>T;
    while(T--) {
        int P; cin>>P;
        cout<<"Case #"<<caseno++<<": ";
        vi f = primeFactors(P);
        int sum = 0;
        for(int &i:f) sum += i;
        if(sz(f)>100 || sum > 41 || 41-sum > 100-sz(f)) {
            cout<<-1<<endl;
            continue;
        }

        cout<<sz(f) + (41-sum)<<" ";
        while(sum < 41) {
            sum++;
            cout<<1<<" ";
        }
        for(int &i:f)
            cout<<i<<" ";
        cout<<endl;
    }
}
