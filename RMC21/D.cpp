int main() {
    int N; cin>>N;
    vi A(N), B(N);
    for(int &i:A)
        cin>>i;
    for(int &i:B)
        cin>>i;
    vi space(N,0);
    for(int i=0; i<N; ++i) {
        MA[A[i]]++;
        MB[B[i]]++;
        if(MA == MB) {
            space[i] = 1;
            MA.clear();
            MB.clear();
        }
    }
    for(int i=0; i<N; ++i) {
        cout<<B[i]<<" ";
        if(i<N-1 && space[i])
            cout<<"# ";
    }
    cout<<endl;
}