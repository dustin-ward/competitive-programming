int main() {
    int N; cin>>N;
    vi V(N);
    for(int &i:V)
        cin>>i;

    vector<ll> prefix(N+2), suffix(N+2);
    prefix[1] = V[0];
    for(int i=2; i<=N; i++)
        prefix[i] = lcm(prefix[i-1],V[i-1]);
    
    suffix[N] = V[N-1];
    for(int i=N-1; i>0; i--)
        suffix[i] = lcm(suffix[i+1],V[i-1]);

    ll ans = 0;
    int steal;
    for(int i=1; i<=N; i++) {
        ll remLCM;
        if(i==1) remLCM = suffix[2];
        else if(i==N) remLCM = prefix[N-1];
        else remLCM = lcm(prefix[i-1],suffix[i+1]);
        if(remLCM > ans) {
            ans = remLCM;
            steal = V[i-1];
        } else if(remLCM == ans && V[i-1] < steal) {
            steal = V[i-1];
        }
    }
    cout<<steal<<endl;
}
