int main() {
    string line;
    while(getline(cin, line)) {
        string ans = "";
        char c = line[0];
        int cur = 1;
        for(int i=1; i<line.length(); ++i) {
            if(line[i] != c) {
                ans += to_string(cur) + c;
                c = line[i];
                cur = 1;
            }
            else
                cur++;
        }
        ans += to_string(cur) + c;
        cout<<ans<<endl;
    }
}