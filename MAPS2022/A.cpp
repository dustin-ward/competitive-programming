int main() {
	int N,S,M; cin>>N>>S>>M;
	vi A(N);
	for(int &i:A)
		cin>>i;

	int h = 0;
	int pos = S-1;
	vi visited(N, 0);
	while(1) {
		if(A[pos] == M) {
			cout<<"magic\n"<<h<<endl;
			return 0;
		}
		if(pos < 0) {
			cout<<"left\n"<<h<<endl;
			return 0;
		}
		if(pos >= N) {
			cout<<"right\n"<<h<<endl;
			return 0;
		}
		if(visited[pos]) {
			cout<<"cycle\n"<<h<<endl;
			return 0;
		}
		h++;
		visited[pos] = 1;
		//if(A[pos] < 0)
		//	pos -= N-abs(A[pos]);
		//else
		//	pos += N-A[pos];
		pos += A[pos];
	}
}
