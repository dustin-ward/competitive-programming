const int MOD = 1000000007;

int main() {
    int N; cin>>N;
    string S; cin>>S;

    int n=0;
    int a=0;
    for(char c:S) {
        n += (c != 'F');
        a += (c == 'A');
    }
    
    ll ans = 0;

    // ans = (2^n - 1) + (2^(n-1) - (n-1))
    int temp = 1;
    for(int i=0; i<n; ++i)
        temp = (temp<<1) % MOD;
    ans = ans + (temp-1) % MOD;

    temp = 1;
    for(int i=0; i<n-1; ++i)
        temp = (temp<<1) % MOD;
    ans = ans + ((temp-1) - (n-1)) % MOD;

    cout<<ans<<endl;
}