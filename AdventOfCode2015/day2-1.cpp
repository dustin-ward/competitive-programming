int main() {
	string s;
	int totAns = 0;
	while(getline(cin,s)) {
		istringstream iss(s);
		char g;
		int L,W,H; iss>>L>>g>>W>>g>>H;
//		debug(L); debug(W); debug(H);
		int ans = 0;
		int lw = L*W;
		int lh = L*H;
		int wh = W*H;
		ans += 2*lw + 2*lh + 2*wh;
		ans += min(min(lw,lh),wh);
		totAns += ans;
	}
	cout<<totAns<<endl;
}
