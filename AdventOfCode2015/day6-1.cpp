vector<vector<bool>> B;

int main() {
	B.resize(1000, vector<bool>(1000,0));
	string s;
	while(getline(cin,s)) {
		istringstream iss(s);
		string inst;
		iss>>inst;
		if(inst == "toggle") {
			int x1,y1,x2,y2;
			char c;
			string g;
			iss>>x1>>c>>y1>>g>>x2>>c>>y2;
			for(int i=y1; i<=y2; ++i)
				for(int j=x1; j<=x2; ++j)
					B[i][j] = !B[i][j];
		}
		else {
			iss>>inst;
			int x1,y1,x2,y2;
			char c;
			string g;
			iss>>x1>>c>>y1>>g>>x2>>c>>y2;
			for(int i=y1; i<=y2; ++i)
				for(int j=x1; j<=x2; ++j)
					B[i][j] = inst == "on";
		}
	}
	int ans = 0;
	for(int i=0; i<1000; ++i)
		for(int j=0; j<1000; ++j)
			ans += B[i][j];

	cout<<ans<<endl;
}
