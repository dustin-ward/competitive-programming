const string VOW = "aeiuo";

bool isVowel(char c) {
	return VOW.find(c) != -1;
}

bool f1(const string &s) {
	int vowCount = 0;
	for(char c:s)
		vowCount += isVowel(c);
	return vowCount >= 3;
}

bool f2(const string &s) {
	for(int i=0; i<sz(s)-1; ++i)
		if(s[i] == s[i+1])
			return true;
	return false;
}

bool f3(const string &s) {
	return s.find("ab") == -1 &&
		   s.find("cd") == -1 &&
		   s.find("pq") == -1 &&
		   s.find("xy") == -1;
}

int main() {
	int ans = 0;
	string s;
	while(getline(cin,s))
		ans += f1(s) && f2(s) && f3(s);
	cout<<ans<<endl;
}
