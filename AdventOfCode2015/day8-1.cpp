int main() {
	int code=0,literal=0;
	string s;
	while(getline(cin,s)) {
		code += sz(s);
		for(int i=0; i<sz(s); ++i) {
			if(s[i] == '\\') {
				assert(i < sz(s)-1);
				if(s[i+1] == '\\' || s[i+1] == '\"') {
					i++;
				}
				else if(s[i+1] == 'x') {
					i+=3;
				}
			}
			literal++;
		}
		literal-=2;
	}
	debug(code);
	debug(literal);
	cout<<code-literal<<endl;
}
