struct UF {
	vi e;
	UF(int n) : e(n, -1) {}
	bool sameSet(int a, int b) { return find(a) == find(b); }
	int size(int x) { return -e[find(x)]; }
	int find(int x) { return e[x] < 0 ? x : e[x] = find(e[x]); }
	bool join(int a, int b) {
		a = find(a), b = find(b);
		if (a == b) return false;
		if (e[a] > e[b]) swap(a, b);
		e[a] += e[b]; e[b] = a;
		return true;
	}
};

int main() {
    int P,T; cin>>P>>T;
    vector<set<int>> V(P);

    int p,t;
    while(cin>>p>>t)
        V[p-1].insert(t);

    UF uf(P);
    for(int i=0; i<P; i++) {
        for(int j=i+1; j<P; j++)
            if(V[i] == V[j])
                uf.join(i,j);
    }

    set<int> S;
    for(int i=0; i<P; i++)
        S.insert(uf.find(i));

    cout<<sz(S)<<endl;
}
