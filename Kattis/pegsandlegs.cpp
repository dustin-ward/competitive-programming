int L,P;
vector<pair<double,double>> dp;
vi drop;

int main() {
  cin>>L>>P;
  dp.resize(L+P);
  drop.resize(L+P, 1);

  for(int i=0; i<L; ++i) {
    cin>>dp[i].fst;
    dp[i].snd = 0.0;
  }

  for(int i=L; i<L+P; ++i) {
    double l,r; cin>>l>>r;
    int x,y; cin>>x>>y;
    x--; y--;
    drop[x] = drop[y] = 0;

    dp[i].fst = (l * dp[x].fst) + (r * dp[y].fst);
    dp[i].snd = (l * dp[x].snd) + (r * dp[y].snd) + (1-l-r);
  }

  double ans = 0;
  for(int i=0; i<L+P; ++i) {
    if(drop[i])
      ans = max(ans, dp[i].fst / (1-dp[i].snd));
  }

  cout<<fixed<<setprecision(10)<<ans<<endl;
}
