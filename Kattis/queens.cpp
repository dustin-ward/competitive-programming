int main() {
	int N; cin>>N; cin.ignore();
	vector<bool> R(N,0), C(N,0), DR((N*2)-1,0), DL((N*2)-1,0);
	bool good = true;
	vector<string> S(N);
	for(string &s:S)
		getline(cin, s);

	for(int i=0; i<N; ++i) {
		istringstream iss(S[i]);
		int x,y; iss>>x>>y;
		if(R[y]) {
			good = false;
			break;
		} else R[y] = 1;

		if(C[x]) {
			good = false;
			break;
		} else C[x] = 1;

		if(DR[(y-x)+N-1]) {
			good = false;
			break;
		} else DR[(y-x)+N-1] = 1;

		if(DL[x+y]) {
			good = false;
			break;
		} else DL[x+y] = 1;
	}
	cout<<(good ? "CORRECT" : "INCORRECT")<<endl;
}
