int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    int N, caseno=1; cin>>N;
    while(N--) {
        int G; cin>>G;
        set<int> s;
        for(int i=0; i<G; i++) {
            int C; cin>>C;
            auto it = s.find(C);
            if(it != s.end())
                s.erase(it);
            else
                s.insert(C);
        }
        cout<<"Case #"<<caseno++<<": "<<*s.begin()<<endl;
    }  
}