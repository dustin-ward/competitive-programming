int main() {
    int N;
    while(cin>>N) {
        bool isPQ=1, isS=1, isQ=1;
        priority_queue<int> PQ;
        stack<int> S;
        queue<int> Q;
        while(N--) {
            int op,arg; cin>>op>>arg;
            if(op == 1) {
                Q.push(arg);
                PQ.push(arg);
                S.push(arg);
            } else {
                if(Q.empty()) isQ=0;
                if(S.empty()) isS=0;
                if(PQ.empty()) isPQ=0;
                if(isQ && Q.front() != arg) isQ=0;
                if(isPQ && PQ.top() != arg) isPQ=0;
                if(isS && S.top() != arg) isS=0;
                if(isQ) Q.pop();
                if(isPQ) PQ.pop();
                if(isS) S.pop();
            }
        }
        int ans = isPQ+isS+isQ;
        if(ans==0)
            cout<<"impossible"<<endl;
        else if(ans>1)
            cout<<"not sure"<<endl;
        else {
            if(isPQ)
                cout<<"priority queue"<<endl;
            else if(isS)
                cout<<"stack"<<endl;
            else
                cout<<"queue"<<endl;
        }
    }
}
