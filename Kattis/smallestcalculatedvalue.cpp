const string OP = "+-*/";
int a,b,c;

int eval(char op1, char op2) {
    int ans;
    switch(op1) {
        case '+':
            ans = a+b;
            break;
        case '-':
            ans = a-b;
            break;
        case '*':
            ans = a*b;
            break;
        case '/':
            if(a%b) return INT_MAX;
            ans = a/b;
            break;
    }
    switch(op2) {
        case '+':
            ans = ans+c;
            break;
        case '-':
            ans = ans-c;
            break;
        case '*':
            ans = ans*c;
            break;
        case '/':
            if(ans%c) return INT_MAX;
            ans = ans/c;
            break;
    }
    return (ans>=0)?ans:INT_MAX;
}

int main() {
    cin>>a>>b>>c;
    
    int ans = INT_MAX;
    for(int i=0; i<4; ++i) {
        for(int j=0; j<4; ++j)
            ans = min(ans, eval(OP[i],OP[j]));
    }
    cout<<ans<<endl;
}
