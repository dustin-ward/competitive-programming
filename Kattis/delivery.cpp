int N,K;

int f(vpii &V) {
    int dis = V[sz(V)-1].fst * 2;
    int L = K;
    for(int i=sz(V)-1; i>=0; i--) {
        if(L >= V[i].snd) {
            L -= V[i].snd;
            V.pop_back();
        } else {
            V[i].snd -= L;
            break;
        }
    }
    return dis;
}

int main() {
    cin>>N>>K;
    vpii pos,neg;
    for(int i=0; i<N; i++) {
        int x,t; cin>>x>>t;
        if(x<0) neg.emplace_back(-x,t);
        else pos.emplace_back(x,t);
    }
    sort(all(pos));
    sort(all(neg));
    int ans = 0;
    while(sz(pos)) ans += f(pos);
    while(sz(neg)) ans += f(neg);
    cout<<ans<<endl;
}
