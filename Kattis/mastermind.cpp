int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    int N;
    string a, b;
    cin>>N>>a>>b;
    
    int r=0,s=0;
    map<char, int> m1,m2;
    for(int i=0; i<a.length(); ++i) {
        r += a[i] == b[i];
        ++m1[a[i]];
        ++m2[b[i]];
    }

    for(auto &p:m1) {
        if(m2.find(p.first) != m2.end())
            s += min(m2[p.first], p.second);
    }
    s -= r;
    printf("%d %d\n", r, s);
}