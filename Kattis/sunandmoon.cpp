int main() {
    int ds,ys,dm,ym;
    cin>>ds>>ys>>dm>>ym;

    vi M,S;
    int m=-dm;
    for(int i=0; i<5001; i++) {
        m+=ym;
        M.push_back(m);
    }
    int s=-ds;
    for(int i=0; i<5001; i++) {
        s+=ys;
        S.push_back(s);
    }

    int pm=0,ps=0;
    while(pm<5001&&ps<5001) {
        if(S[ps]==M[pm]&&S[ps]>0&&M[pm]>0) {
            cout<<S[ps]<<endl;
            return 0;
        }
        if(S[ps]<M[pm]) ps++;
        else pm++;
    }
}
