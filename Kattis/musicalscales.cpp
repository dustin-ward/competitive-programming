vector<string> notes = {"A", "A#", "B", "C", "C#", "D", "D#", "E", "F", "F#", "G", "G#"};
vector<int> scale = {2, 2, 1, 2, 2, 2};

int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    int N; cin>>N;
    vector<string> song(N);
    for(int i=0; i<N; i++)  
        cin>>song[i];

    bool none = true;
    for(int s=0; s<12; s++) {
        set<string> S;
        int pos = s;
        S.insert(notes[s]);
        for(int i=0; i<6; i++) {
            pos = (pos + scale[i]) % 12;
            S.insert(notes[pos]);
        }
        
        bool good = true;
        for(string x:song) {
            if(S.find(x) == S.end()) {
                good = false;
                break;
            }
        }
        if(good) {
            none = false;
            cout<<notes[s]<<" ";
        }
    }
    if(none)
        cout<<"none";
    cout<<endl;
}