unordered_map<int,vi> m;
ll dp[401];

ll DFS(int n) {
    if(dp[n] != -1) return dp[n];

    ll sum = 0;
    for(int i:m[n])
        sum += DFS(i);
    return dp[n] = sum;
}

int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    int T; cin>>T;
    while(T--) {
        int S; cin>>S;
        m.clear();
        for(int i=0; i<401; i++)
            dp[i] = -1;

        for(int i=0; i<S; i++) {
            int p,a,b,c;
            string s;
            cin>>p>>s;
            if(s == "favourably")
                dp[p-1] = 1;
            else if(s == "catastrophically")
                dp[p-1] = 0;
            else {
                a = stoi(s);
                cin>>b>>c;
                m[p-1] = {a-1, b-1, c-1};
            }
        }

        ll ans = DFS(0);
        cout<<ans<<endl;
    }
}