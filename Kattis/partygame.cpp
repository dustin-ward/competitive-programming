int main() {
	int T; cin>>T;
	while(T--) {
		int N; cin>>N;
		vi A(N);
		for(int &i:A)
			cin>>i;

		for(int i=0; i<N; ++i) {
			vi A2(N);
			for(int j=0; j<N; j++)
				A2[j] = A[A[j]-1];
			A = A2;
		}

		bool valid = true;
		for(int i=1; i<N; ++i) {
			if(A[i] < A[i-1]) {
				valid = false;
				break;
			}
		}
		if(valid)
			cout<<"All can eat."<<endl;
		else
			cout<<"Some starve."<<endl;
	}
}
