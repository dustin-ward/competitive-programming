const int P = 1000000007;

int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    ll ans = 0;
    ll _2k = 1;
    ll half_2k = 1;
    ll num0 = 0;
    ll num1 = 0;
    ll numQ = 0;

    char c;
    while(cin>>c) {
        switch(c) {
            case '0':
                ans = (ans + ((num1 * _2k) % P)) % P;
                ans = (ans + ((numQ * half_2k) % P)) % P;

                num0++;
                break;
            case '1':
                num1++;
                break;
            case '?':
                ans = (ans*2) % P;
                ans = (ans + ((num1 * _2k) % P)) % P;
                ans = (ans + ((numQ * half_2k) % P)) % P;

                half_2k = _2k;
                _2k = (_2k*2) % P;
                numQ++;
                break;
        }
    }

    cout<<ans%P<<endl;
}