struct UF {
	vi e;
	UF(int n) : e(n, -1) {}
	bool sameSet(int a, int b) { return find(a) == find(b); }
	int size(int x) { return -e[find(x)]; }
	int find(int x) { return e[x] < 0 ? x : e[x] = find(e[x]); }
	bool join(int a, int b) {
		a = find(a), b = find(b);
		if (a == b) return false;
		if (e[a] > e[b]) swap(a, b);
		e[a] += e[b]; e[b] = a;
		return true;
	}
};

map<string,int> M;
int getId(string s) {
    if(!M.count(s))
        M[s] = sz(M);
    return M[s];
}

int main() {
    int N; cin>>N;
    UF uf(N*2);
    while(N--) {
        string a, b; cin>>a>>b;
        int u = getId(a);
        int v = getId(b);
        uf.join(u,v);
        cout<<uf.size(u)<<endl;
    }
}
