int main() {
	int N;
	bool flag=false;
	while(cin>>N && N) {
		cin.ignore();
		string line;
		
		if(flag)
			cout<<"\n";
		else
			flag = true;

		int w = -1;
		bool err = false;
		while(N--) {
			getline(cin,line);
			istringstream iss(line);

			char c; iss>>c;
			bool hash = c == '#';
			int n;
			int count = 0;
			while(iss>>n) {
				for(int i=0; i<n; ++i,++count)
					cout<<(hash?'#':'.');
				hash = !hash;
			}
			cout<<"\n";

			if(w == -1)
				w = count;
			else if(w != count)
				err = true;
		}
		if(err)
			cout<<"Error decoding image\n";
	}
}
