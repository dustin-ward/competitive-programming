struct L {
	int x;
	int y;
	int r;
	double d;
};

int main() {
	int cx,cy,N; cin>>cx>>cy>>N;
	vector<L> V(N);
	for(L &l:V) {
		cin>>l.x>>l.y>>l.r;
		double dx = (l.x-cx)*(l.x-cx);
		double dy = (l.y-cy)*(l.y-cy);
		l.d = sqrt(dx+dy) - l.r;
	}

	sort(all(V), [](const L &a, const L &b) {return a.d < b.d;});
	cout<<max((int)V[2].d,0)<<endl;
}
