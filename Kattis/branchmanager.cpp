int N,M;
vector<priority_queue<int>> G;
queue<int> Q;
set<int> S;

void f(int n) {
    if(Q.empty()) return;
    auto p = S.insert(n);
    assert(p.snd);
    
    while(!G[n].empty()) {
        int x = -G[n].top();
        G[n].pop();
        f(x);
    }
    
    while(!Q.empty() && S.count(Q.front()))
        Q.pop();

    S.erase(p.fst);
}

int main() {
    cin>>N>>M;

    G.resize(N);
    for(int i=0; i<N-1; i++) {
        int u,v; cin>>u>>v;
        G[u-1].push(-(v-1));
    }

    for(int i=0; i<M; i++) {
        int temp; cin>>temp;
        Q.push(temp-1);
    }

    f(0);

    cout<<M-sz(Q)<<endl;
}
