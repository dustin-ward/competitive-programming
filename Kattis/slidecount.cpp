int main() {
    int N,C; cin>>N>>C;
    vi W(N);
    for(int &i:W)
        cin>>i;
    
    vi in(N), out(N);
    ll s=0,e=0,sum=W[0],t=1;
    while(s<N) {
        if(e+1>=N) {
            out[s] = t;
            sum -= W[s];
            s++;
        }
        else if(sum+W[e+1] > C) {
            out[s] = t;
            sum -= W[s];
            s++;
        }
        else {
            e++;
            in[e] = t;
            sum += W[e];
            
        }
        sum = max(sum, (ll)0);
        t++;
    }

    for(int i=0; i<N; ++i)
        cout<<out[i]-in[i]<<endl;
}