int C,N;
int W[2001], V[2001];

int main() {
  while(cin>>C>>N) {
    for(int i=1; i<=N; ++i)
      cin>>V[i]>>W[i];
    W[0] = V[0] = 0;

    vector<vi> dp(N+1, vi(C+1, 0));
    for(int i=1; i<=N; ++i) {
      for(int j=1; j<=C; ++j) {
        if(W[i] <= j)
          dp[i][j] = max(V[i] + dp[i-1][j - W[i]], dp[i-1][j]);
        else
          dp[i][j] = dp[i-1][j];
      }
    }

    vi path;
    int j = C;
    for(int i=N; i>0; --i) {
      if(dp[i][j] != dp[i-1][j]) {
        path.push_back(i-1);
        j -= W[i];
      }
    }

    cout<<path.size()<<endl;
    for(auto i:path)
      cout<<i<<" ";
    cout<<endl;
  }
}
