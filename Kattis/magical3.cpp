vi ans;
void numToBase(int n, int b) {
    ans.clear();
    while(n) {
        ans.push_back(n % b);
        n /= b; 
    }
}

int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    int n;
    while(cin>>n && n) {
        n -= 3;
        if(n < 0) {
            cout<<"No such base"<<endl;
            continue;
        }
        else if(n == 0) {
            cout<<4<<endl;
            continue;
        }

        ll ans = (ll)INT_MAX+1;
        for(ll b=1; b*b<=n; ++b) {
            if(n%b == 0) {
                if(b >= 4)
                    ans = min(ans, b);
                if(n/b >= 4)
                    ans = min(ans, n/b);
            }
        }
        if(ans != (ll)INT_MAX+1)
            cout<<ans<<endl;
        else
            cout<<"No such base"<<endl;
    }
}