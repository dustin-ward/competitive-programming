ll N,M;
vector<pair<ll,ll>> I;

bool f(ll d) {
    ll tot = 0;
    for(auto &[p,c]:I) {
        ll repay = (c+p-1)/p;
        if(repay>d) break;

        tot += (p*d)-c;
    }
    return tot>=M;
}

int main() {
    cin>>N>>M;
    I.resize(N);
    for(int i=0; i<N; i++)
        cin>>I[i].fst>>I[i].snd;
    sort(all(I),[](const pair<ll,ll> &a,const pair<ll,ll> &b){
        return (a.snd+(a.fst-1))/a.fst < (b.snd+(b.fst-1))/b.fst;
    });

    ll lo=0,hi=1;
    while(!f(hi)) hi*=2;
    while(hi-lo>1) {
        ll mid = (lo+hi)/2;
        if(f(mid))
            hi = mid;
        else
            lo = mid;
    }
    cout<<hi<<endl;
}
