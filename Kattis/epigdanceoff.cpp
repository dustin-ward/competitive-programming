int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    int N,M; cin>>N>>M;
    vector<vector<char>> V(N, vector<char>(M));
    for(int i=0; i<N; i++) {
        for(int j=0; j<M; j++)
            cin>>V[i][j];
    }

    int ans = 0;
    for(int c=0; c<M; c++) {
        bool clear = true;
        for(int r=0; r<N; r++)
            clear &= V[r][c] == '_';
        ans += clear;
    }

    cout<<ans+1<<endl;
}