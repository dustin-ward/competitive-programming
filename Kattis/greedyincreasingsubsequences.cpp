vector<deque<int>> seq;
int ans=1;
int bin(int x) {
    int lo=0,hi=ans;

    while(lo<hi) {
        int m = (lo+hi)/2;
        if(seq[m].back() >= x)
            lo = m+1;
        else
            hi = m;
    }

    return hi;
}

int main() {
    int N; cin>>N;
    vi A(N);
    for(int &i:A)
        cin>>i;

    seq.resize(2);
    seq[0].push_back(-1);
    seq[0].push_back(A[0]);
    seq[1].push_back(-1);
    for(int i=1; i<N; i++) {
        int idx = bin(A[i]);
        seq[idx].push_back(A[i]);
        if(idx==ans) {
            ans++;
            seq.push_back(deque<int>());
            seq[ans].push_back(-1);
        }
    }

    cout<<ans<<endl;
    for(int i=0; i<ans; i++) {
        seq[i].pop_front();
        while(!seq[i].empty()) {
            cout<<seq[i].front()<<" ";
            seq[i].pop_front();
        }
        cout<<endl;
    }
}
