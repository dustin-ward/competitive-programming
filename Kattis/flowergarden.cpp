int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    vector<bool> prime(20001, 1);
    prime[1] = 0;
    for(int i=2; i*i <= 20001; i++) {
        if (prime[i]) {
            for (int j=i*i; j <= 20001; j += i)
                prime[j] = 0;
        }
    }

    int T; cin>>T;
    while(T--) {
        ld N,D; cin>>N>>D;
        int ans = 0;
        ld curX=0, curY=0, lastPrime=0, total=0;
        for(int i=0; i<N; i++) {
            ld x,y; cin>>x>>y;
            ld dis = sqrt(pow(static_cast<long double>(x-curX), 2) +
			              pow(static_cast<long double>(y-curY), 2));
            total += dis;
            if(total <= D) {
                ans++;
            }
            if(prime[ans])
                lastPrime = ans;
            curX = x;
            curY = y;
        }

        cout<<lastPrime<<endl;
    }
}