int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    int C,N,M; cin>>C>>N>>M;
    vector<vector<ll>> dp(51, vector<ll>(1001, 0));
    for(int i=0; i<N; i++) {
        int temp; cin>>temp;
        dp[0][temp]++;
    }

    for(int i=0; i<50; i++) {
        for(int j=1; j<=C; j++) {
            if(j*2 <= C)
                dp[i+1][j*2] += dp[i][j];
            else
                dp[i+1][j] += dp[i][j] * 2;
        }
    }

    for(int i=0; i<M; i++) {
        int q; cin>>q;
        ll ans = 0;
        for(int j=1; j<=C; j++)
            ans += dp[q][j];
        cout<<ans<<endl;
    }
}