struct Point {
  double x, y;
};

double area_polygon(Point polygon[], int n)
{
  double sum = 0.0;

  for (int i = 0; i < n-1; i++) {
    sum += polygon[i].x * polygon[i+1].y - polygon[i].y * polygon[i+1].x;
  }
  sum += polygon[n-1].x * polygon[0].y - polygon[n-1].y * polygon[0].x;
  return sum/2.0;
}

int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    int N;
    while(cin>>N && N) {
        Point p[1001];
        for(int i=0; i<N; i++) {
            int x,y; cin>>x>>y;
            p[i].x = x;
            p[i].y = y;
        }

        double ans = area_polygon(p, N);
        cout<<((ans > 0) ? "CCW " : "CW ")<<fixed<<setprecision(1)<<fabs(ans)<<endl;
    }
}