int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    int N,P2; double P; cin>>N>>P;
    P2 = int(P * 10000 + 0.5);
    vector<int> v(10001, INT_MAX);
    v[0] = 0;

    for(int i=0; i<N; i++) {
        int e,p2; double p; cin>>e>>p;
        p2 = int(p * 10000 + 0.5);
        for(int j=10000; j>=0; j--) {
            if(v[j] == INT_MAX)
                continue;
            v[j+p2] = min(v[j+p2], v[j] + e);
        }
    }

    int ans = INT_MAX;
    for(int i=P2; i<=10000; i++)
        ans = min(v[i], ans);
    cout<<ans<<endl;
}
