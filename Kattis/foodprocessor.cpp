double f(double t, double s, double h) {
    return (h * log(t/s)) / (log(2));
}

int main() {
    int S,T,N; cin>>S>>T>>N;

    priority_queue<pii> pq;
    for(int i=0; i<N; i++) {
        int m,h; cin>>m>>h;
        pq.emplace(m,h);
    }

    if(pq.top().fst < S) {
        cout<<-1<<endl;
        return 0;
    }

    double ans = 0.0;
    int b = pq.top().snd;
    pii cur;
    while(S!=T) {
        cur = pq.top();
        b = min(b, cur.snd);
        pq.pop();

        while(!pq.empty() && pq.top().fst >= S) {
            cur = pq.top();
            b = min(b, cur.snd);
            pq.pop();
        }

        int t = pq.empty()?T:max(T,pq.top().fst);
        ans += f(t, S, b);
        S=t;
    }

    cout<<scientific<<setprecision(20)<<-ans<<endl;
}
