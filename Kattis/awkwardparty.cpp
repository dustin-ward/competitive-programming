int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    int N; cin>>N;
    map<int, vi> M;
    for(int i=0; i<N; ++i) {
        int temp; cin>>temp;
        M[temp-1].push_back(i);
    }

    int ans = INT_MAX;
    for(auto &P:M) {
        vi &V = P.second;
        if(sz(V) > 1) {
            for(int i=1; i<sz(V); ++i)
                ans = min(ans, abs(V[i]-V[i-1]));
        }
    }

    if(ans == INT_MAX)
        cout<<N<<endl;
    else
        cout<<ans<<endl;
}