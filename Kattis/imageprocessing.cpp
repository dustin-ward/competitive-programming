int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    int H,W,N,M; cin>>H>>W>>N>>M;
    vector<vi> image(H, vi(W)), kernel(N, vi(M)), output(H-N+1, vi(W-M+1, 0));
    for(int i=0; i<H; i++) {
        for(int j=0; j<W; j++)
            cin>>image[i][j];
    }
    for(int i=N-1; i>=0; i--) {
        for(int j=M-1; j>=0; j--)
            cin>>kernel[i][j];
    }
    for(int i=0; i<H-N+1; i++) {
        for(int j=0; j<W-M+1; j++) {
            for(int x=0; x<N; x++) {
                for(int y=0; y<M; y++)
                    output[i][j] += image[i+x][j+y] * kernel[x][y];
            }
        }
    }

    for(int i=0; i<H-N+1; i++) {
        for(int j=0; j<W-M+1; j++)
            cout<<output[i][j]<<" ";
        cout<<endl;
    }

}