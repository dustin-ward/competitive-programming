int main() {
    ll N,S,K;
    cin>>N>>S>>K;

    vi X(N);
    for(ll &i:X)
        cin>>i;

    sort(all(X));
    for(int i=0; i<N-1; ++i) {
        if(X[i+1]-X[i] < S) {
            cout<<-1<<endl;
            return 0;
        }
    }

    vi Y(N,S);
    for(int i=0; i<N; ++i) {
        double roomLeft=INT_MAX,roomRight=INT_MAX;

        if(i)
            roomLeft = (X[i]-X[i-1]) - (Y[i-1]/2.0);
        if(i<N-1)
            roomRight = (X[i+1]-X[i]) - (Y[i+1]/2.0);

        double minRoom = min(roomLeft,roomRight);
        Y[i] = min(K,(ll)(minRoom*2));
    }

    ll sum = 0;
    for(ll &i:Y)
        sum += i;
    cout<<sum<<endl;
}
