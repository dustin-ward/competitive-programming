int N;

bool f(vi &v, int w) {
    int curW = 0;
    for(int i=0; i<sz(v); i++) {
        if(v[i] < w) continue;
        if(v[i] == curW) curW = 0;
        else if(curW) return false;
        else curW = v[i];
    }
    return true;
}

int main() {
    cin>>N;
    vi top(N),bottom(N);

    map<int,int> freq;
    for(int &i:top) {
        cin>>i;
        freq[i]++;    
    }
    for(int &i:bottom)
        cin>>i;

    int lo=0,hi=1000000001;
    for(pii p:freq)
        if(p.snd==1)
            lo = max(lo,p.fst);

    while(hi-lo>1) {
        int mid = (lo+hi)/2;
        if(f(top,mid) && f(bottom,mid))
            hi = mid;
        else
            lo = mid;
    }
    cout<<lo<<endl;
}
