int main() {
    int N;
    while(cin>>N && N) {
        vi C(N);
        int sum = 0;
        for(int i=0; i<N; ++i) {
            double temp; cin>>temp;
            C[i] = 100 * temp + 0.1;
            sum += C[i];
        }
        int avg = sum/N;
        int over = 0;
        int numOver = 0;
        for(int i:C) {
            if(i>avg) {
                over += i-avg;
                numOver++;
            }
        }
        int ans;
        if(numOver > sum%N)
            ans = over - sum%N;
        else
            ans = over - numOver;
        cout<<fixed<<setprecision(2)<<"$"<<(double)ans/100.0<<endl;
    }
}