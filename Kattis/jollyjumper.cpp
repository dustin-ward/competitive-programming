int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    int N;
    while(cin>>N) {
        int last; cin>>last;
        vector<bool> B(N, 0);
        for(int i=1; i<N; i++) {
            int temp;
            cin>>temp;
            B[abs(temp-last)] = 1;
            last = temp;
        }

        bool valid = true;
        for(int i=1; i<N; i++) {
            if(!B[i]) {
                valid = false;
                break;
            }
        }

        if(valid)
            cout<<"Jolly"<<endl;
        else
            cout<<"Not jolly"<<endl;
    }
}