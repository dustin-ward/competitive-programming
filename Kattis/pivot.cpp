int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    set<int> P;
    int N; cin>>N;
    int x; cin>>x; --N;
    int maxX = x;
    P.insert(x);
    while(N--) {
        cin>>x;
        if(x > maxX) {
            maxX = x;
            P.insert(x);
        }
        else {
            auto it = P.begin();
            while(it != P.end()) {
                if(*it > x)
                    it = P.erase(it);
                else
                    ++it;
            }
        }
    }
    cout<<P.size()<<endl;
}