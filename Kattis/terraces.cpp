class UnionFind
{
      struct UF { int p; int rank; };

   public:
      UnionFind(int n) {          // constructor
	 howMany = n;
	 uf = new UF[howMany];
	 for (int i = 0; i < howMany; i++) {
	    uf[i].p = i;
	    uf[i].rank = 0;
	 }
      }

      ~UnionFind() {
         delete[] uf;
      }

      int find(int x) { return find(uf,x); }        // for client use
      
      bool merge(int x, int y) {
	 int res1, res2;
	 res1 = find(uf, x);
	 res2 = find(uf, y);
	 if (res1 != res2) {
	    if (uf[res1].rank > uf[res2].rank) {
	       uf[res2].p = res1;
	    }
	    else {
	       uf[res1].p = res2;
	       if (uf[res1].rank == uf[res2].rank) {
		  uf[res2].rank++;
	       }
	    }
	    return true;
	 }
	 return false;
      }
      
   private:
      int howMany;
      UF* uf;

      int find(UF uf[], int x) {     // recursive funcion for internal use
	 if (uf[x].p != x) {
	    uf[x].p = find(uf, uf[x].p);
	 }
	 return uf[x].p;
      }
};

int x,y; 

int f(int i, int j) {
    return i*x + j;
}

int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    cin>>x>>y;
    vector<vi> G(y, vi(x));
    vector<bool> pool(x*y, true);
    for(vi &i:G) {
        for(int &j:i)
            cin>>j;
    }

    UnionFind uf(x*y);
    for(int i=0; i<y; i++) {
        for(int j=0; j<x; j++) {
            if(i>0) {
                if(G[i][j] == G[i-1][j])
                    uf.merge(f(i,j), f(i-1,j));
                else if(G[i][j] < G[i-1][j])
                    pool[f(i-1,j)] = 0;
                else
                    pool[f(i,j)] = 0;
            }
            if(j>0) {
                if(G[i][j] == G[i][j-1])
                    uf.merge(f(i,j), f(i,j-1));
                else if(G[i][j] < G[i][j-1])
                    pool[f(i,j-1)] = 0;
                else
                    pool[f(i,j)] = 0;
            }
        }
    }

    for(int i=0; i<x*y; i++) {
        if(!pool[i])
            pool[uf.find(i)] = 0;
    }

    int ans = 0;
    for(int i=0; i<x*y; i++)
        ans += pool[uf.find(i)];

    cout<<ans<<endl;
}