int T;
int N1,N2;
int S1,S2;

ll DP[2001][2001];

ll f(int a, int b) {
    ll &ans = DP[a][b];
    if(ans != -1) return ans;

    ans = INT_MAX;
    for(int d=0; d<=b && d*S2 <= T; d++) {
        int c = min(a,(T-(d*S2))/S1);
        if(c||d) ans = min(ans, 1+f(a-c,b-d));
    }

    return ans;
}

int main() {
    cin>>T>>N1>>N2>>S1>>S2;
    for(int i=0; i<=N1; i++)
        for(int j=0; j<=N2; j++)
            DP[i][j] = -1;

    DP[0][0] = 0;
    cout<<f(N1,N2)<<endl;
}
