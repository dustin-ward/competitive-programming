int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    int N,T,K; cin>>N>>T>>K;
    ll P = 0;
    vi hand(T,0);
    vpii cost(T);
    for(int i=0; i<N; i++) {
        int temp; cin>>temp;
        hand[temp-1]++;
    }

    for(int i=0; i<T; i++) {
        int a,b; cin>>a>>b;
        cost[i] = make_pair(max(2-hand[i], 0)*a, hand[i]*b);
    }

    sort(cost.begin(), cost.end(), [](pii &a, pii &b){ return a.first+a.second <= b.first+b.second;});

    for(int i=0; i<K; i++)
        P -= cost[i].first;
    for(int i=K; i<T; i++)
        P += cost[i].second;

    cout<<P<<endl;
}