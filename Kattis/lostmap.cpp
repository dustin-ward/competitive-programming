class UnionFind
{
      struct UF { int p; int rank; };

   public:
      UnionFind(int n) {          // constructor
	 howMany = n;
	 uf = new UF[howMany];
	 for (int i = 0; i < howMany; i++) {
	    uf[i].p = i;
	    uf[i].rank = 0;
	 }
      }

      ~UnionFind() {
         delete[] uf;
      }

      int find(int x) { return find(uf,x); }        // for client use
      
      bool merge(int x, int y) {
	 int res1, res2;
	 res1 = find(uf, x);
	 res2 = find(uf, y);
	 if (res1 != res2) {
	    if (uf[res1].rank > uf[res2].rank) {
	       uf[res2].p = res1;
	    }
	    else {
	       uf[res1].p = res2;
	       if (uf[res1].rank == uf[res2].rank) {
		  uf[res2].rank++;
	       }
	    }
	    return true;
	 }
	 return false;
      }
      
   private:
      int howMany;
      UF* uf;

      int find(UF uf[], int x) {     // recursive funcion for internal use
	 if (uf[x].p != x) {
	    uf[x].p = find(uf, uf[x].p);
	 }
	 return uf[x].p;
      }
};

struct Edge {
    int u;
    int v;
    int w;
};

int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    int n; cin>>n;
    vector<Edge> v;
    vector<vector<int>> map(n, vector<int>(n));
    for(int i=0; i<n; i++) {
        for(int j=0; j<n; j++)
            cin>>map[i][j];
    }

    for(int i=0; i<n; i++) {
        for(int j=i+1; j<n; j++) {
            Edge e;
            e.u = i; e.v = j; e.w = map[i][j];
            v.push_back(e);
        }
    }

    sort(v.begin(), v.end(), [](Edge &a, Edge &b) {return a.w<b.w;});
    vector<Edge> ans;
    UnionFind uf(n);
    for(auto e:v) {
        if(uf.find(e.u) != uf.find(e.v)) {
            ans.push_back(e);
            uf.merge(e.u, e.v);
        }
    }

    for(auto e:ans)
        cout<<e.u+1<<" "<<e.v+1<<endl;
}