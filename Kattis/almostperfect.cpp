int main() {
  int N;
  while(cin>>N) {
    int n = sqrt(N);
    debug(n);
    int sum = 1;
    for(int i=2; i<=n; ++i) {
      if(!(N%i))
        sum += i + (N/i);
    }
    if(n*n==N)
      sum -= n;
    debug(sum);

    if(abs(N-sum) == 0)
      cout<<N<<" perfect"<<endl;
    else if(abs(N-sum) <= 2)
      cout<<N<<" almost perfect"<<endl;
    else
      cout<<N<<" not perfect"<<endl;
  }
}
