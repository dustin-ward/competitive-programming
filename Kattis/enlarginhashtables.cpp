const int seive_size = 40001;
bitset<seive_size> b;
vi p;
void seive() {
    b.set();
    b[0] = b[1] = 0;
    for(int i=2; i<seive_size; i++) if(b[i]) {
        for(int j=i*i; j<seive_size; j+=i)
            b[j] = 0;
        p.push_back(i);
    }
}

bool isPrime(int x) {
    if(x < seive_size) return b[x];

    for(int i=0; i<sz(p) && p[i]*p[i]<=x; i++) {
        if(x%p[i] == 0)
            return false;
    }
    return true;
}

int main() {
    seive();
    int x;
    while(cin>>x && x) {
        int x2 = 2*x;
        while(!isPrime(x2)) x2++;
        cout<<x2<<" ";
        if(!isPrime(x))
            cout<<"("<<x<<" is not prime)"<<endl;
        else
            cout<<endl;
    }
}
