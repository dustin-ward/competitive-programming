int main() {
  string s;
  int caseno=1;
  while(cin>>s && s != "END") {
    s = s.substr(1);
    bool even = true;
    int dis = -1;
    while(sz(s)) {
      int pos = s.find('*');
      if(dis == -1) {
        dis = pos;
        s = s.substr(pos+1);
        continue;
      }

      if(pos != dis) {
        even = false;
        break;
      }
      s = s.substr(pos+1);
    }

    if(even)
      cout<<caseno++<<" EVEN"<<endl;
    else
      cout<<caseno++<<" NOT EVEN"<<endl;
  }
}
