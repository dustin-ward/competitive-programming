#define LSOne(S) ((S) & -(S))

class FenwickTree {
    private:
        vector<ll> ft;
    public:
        FenwickTree(int m) { ft.assign(m+1,0);}
        ll rsq(ll j) {
            ll sum = 0;
            for(;j;j-=LSOne(j))
                sum+=ft[j];
            return sum;
        }
        void update(ll i, ll v) {
            for(; i<(ll)ft.size(); i+=LSOne(i))
                ft[i] += v;
        }
};

int main() {
    int N,Q; cin>>N>>Q;
    FenwickTree ft(N+1);

    string out = "";
    while(Q--) {
        char op; cin>>op;
        if(op == '+') {
            ll i,v; cin>>i>>v;
            ft.update(i+1,v);
        } else {
            ll q; cin>>q;
            if(q)
                out += to_string(ft.rsq(q)) + "\n";
            else
                out += "0\n";
        }
    }
    cout<<out;
}
