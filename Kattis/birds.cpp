int main() {
	ll l,d,n; cin>>l>>d>>n;
	vector<ll> birds(n);
	for(ll &i:birds)
		cin>>i;
	sort(all(birds));

	ll ans = 0;
	ll pos = 6;
	if(!n)
		ans = ((l-12)/d) + 1;
	else {
		for(int i=0; i<n; ++i) {
			while(birds[i] - pos >= d) {
				pos += d;
				ans++;
			}
			pos = birds[i] + d;
		}

		while(pos <= l-6) {
			pos += d;
			ans++;
		}
	}

	cout<<ans<<endl;
}
