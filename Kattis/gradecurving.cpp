double dp[INF];

int main() {
  double X, Y1, Y2; cin>>X>>Y1>>Y2;
  dp[0] = X;

  for(int i=1; i<INF; ++i)
    dp[i] = 10 * sqrt(dp[i-1]);

  int m = -1,M=-1;

  for(int i=0; i<INF; ++i) {
    if(m == -1 && ceil(dp[i]) >= Y1 && ceil(dp[i]) <= Y2)
      m = i;
    if(M == -1 && ceil(dp[i]) > Y2) {
      M = i-1;
      break;
    }
  }

//  for(int i=0; i<20; ++i)
//    cout<<dp[i]<<" ";
//  cout<<endl;

  if(m == -1)
    cout<<"impossible"<<endl;
  else {
    cout<<m<<" ";
    if(M == -1)
      cout<<"inf";
    else
      cout<<M;
    cout<<endl;
  }
}
