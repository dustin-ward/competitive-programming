const int MOD = 10000;
string refr = "welcome to code jam";
string line;

int dp[502][21];

int f(int pos, int count) {
    int ans = 0;
    if(count == refr.length()) return 1;
    if(dp[pos][count] != -1) return dp[pos][count];
    for(int i=pos; i<line.length(); i++) {
        if(line[i] == refr[count])
            ans += f(i+1, count+1) % MOD;
    }
    return dp[pos][count] = ans;
}

int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    int N,caseno=1; cin>>N;
    cin.ignore();
    while(N--) {
        getline(cin, line);
        for(int i=0; i<502; i++) {
            for(int j=0; j<21; j++)
                dp[i][j] = -1;
        }
        
        int ans = f(0,0);
        cout<<"Case #"<<caseno++<<": "<<setw(4)<<setfill('0')<<ans<<endl;
    }
}