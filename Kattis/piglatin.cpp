bool isVowel(char c) {
    c = tolower(c);
    return c == 'a' || c == 'e' || c == 'i' || c == 'o' || c == 'u' || c == 'y';
}

int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    string line, word;
    while(getline(cin, line)) {
        istringstream iss(line);
        while(iss>>word) {
            if(isVowel(word[0]))
                word += "yay";
            else {
                int vowPos = 1;
                while(vowPos < word.length() && !isVowel(word[vowPos]))
                    vowPos++;

                if(vowPos != word.length())
                    word = word.substr(vowPos) + word.substr(0,vowPos);

                word += "ay";
            }
            cout<<word<<" ";
        }
        cout<<endl;
    }
}