#define LSOne(S) ((S) & -(S))

class FenwickTree {
    private:
        vector<ll> ft;
    public:
        FenwickTree(int m) { ft.assign(m+1,0);}
        int rsq(ll j) {
            ll sum = 0;
            for(;j;j-=LSOne(j))
                sum+=ft[j];
            return sum;
        }
        void update(ll i, ll v) {
            for(; i<(ll)ft.size(); i+=LSOne(i))
                ft[i] += v;
        }
};

int main() {
    int N,K; cin>>N>>K;
    FenwickTree ft(N+1);
    while(K--) {
        char op; cin>>op;
        if(op == 'F') {
            int i; cin>>i;
            int b = ft.rsq(i)-ft.rsq(i-1);
            ft.update(i,(b)?-1:1);
        } else {
            int l,r; cin>>l>>r;
            cout<<ft.rsq(r)-ft.rsq(l-1)<<endl;
        }
    }
}
