const int P = 1000000007;

int dp[102][10002];
int N,W,H; 

int f(int w, int n) {
    if(n < 0)
        return 0;
    if(w >= W)
        return 1;
    if(dp[w][n] != -1)
        return dp[w][n];
    ll count = 0;
    for(int i=0; i<=H; i++)
        count += f(w+1, n-i);
    return dp[w][n] = count % P;
}

int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    cin>>N>>W>>H;
    for(int i=0; i<=W; i++) {
        for(int j=0; j<=N; j++)
            dp[i][j] = -1;
    }

    int plainsScenes = (min(N, W*H)/W) + 1;
    cout<<((f(0, N) - plainsScenes) + P) % P<<endl;
}