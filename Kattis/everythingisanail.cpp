int N;
int dp[4][8][300001];
vi A;

int f(int tool, int belt, int pos) {
    if(pos==N) return 0;
    int &ans = dp[tool][belt][pos];
    if(ans!=-1) return ans;

    ans = f(tool,belt,pos+1);

    if(tool>0 && A[pos]==tool-1)
        ans = max(ans, f(tool,belt,pos+1)+1);
    
    for(int i=0; i<3; i++) {
        if(belt & (1<<i))
            ans = max(ans, f(i+1,belt&~(1<<i),pos));
    }

    return ans;
}

int main() {
    for(int i=0; i<4; i++)
        for(int j=0; j<8; j++)
            for(int k=0; k<300001; k++)
                dp[i][j][k]=-1;

    cin>>N;
    A.resize(N);
    for(int &i:A)
        cin>>i;

    cout<<f(0,7,0)<<endl;

}
