const int MAX_N = 10;
ll binom[MAX_N+1][MAX_N+1];
void precomp()
{
  for (int n = 0; n <= MAX_N; n++) {
    binom[n][0] = binom[n][n] = 1;
    for (int k = 1; k < n; k++) {
      binom[n][k] = binom[n-1][k] + binom[n-1][k-1];
    }
  }
}

int main() {
    precomp();
    int N; cin>>N;
    while(N--) {
        int R,S,X,Y,W; cin>>R>>S>>X>>Y>>W;
        double p = 0.0;
        for(int i=X; i<=Y; ++i)
            p += binom[Y][i] * pow((S-(R-1.0))/S, i) * pow((R-1.0)/S, Y-i);
        cout<<(p * W > 1 ? "yes" : "no")<<endl;
    }
}
