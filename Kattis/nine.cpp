const ll MOD = 1E9+7;

ll fast_exp(ll b, ll n)
{
  ll res = 1;
  ll x = b;

  while (n > 0) {
    if (n & 0x01) {
      n--;
      res = (res * x) % MOD;
    } else {
      n >>= 1;
      x = (x*x) % MOD;
    }
  }

  return res;
}

