int main() {
    string s; cin>>s;
    queue<string> Q;
    Q.push(s);

    set<string> ans;
    while(!Q.empty()) {
        s = Q.front();
        Q.pop();
        for(int i=0; i<sz(s); i++) {
            if(s[i] == '(') {
                int d = 0;
                int j;
                for(j=i+1; j<sz(s); j++) {
                    if(s[j] == '(') d++;
                    if(s[j] == ')') {
                        if(d) d--;
                        else break;
                    }
                }
                string s2 = s.substr(0,i) + s.substr(i+1,j-(i+1)) + s.substr(j+1);
                if(ans.count(s2)) continue;
                ans.insert(s2);
                Q.push(s2);
            }
        }
    }

    for(string s:ans)
        cout<<s<<endl;
}
