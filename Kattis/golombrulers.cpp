int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    string line;
    while(getline(cin, line)) {
        istringstream iss(line);
        vi v;
        
        int temp;
        while(iss>>temp)
            v.push_back(temp);
        
        sort(v.rbegin(), v.rend());
        
        int n = v[0];
        vi ans(n+1, 0);
        for(int i=0; i<v.size(); i++) {
            for(int j=i+1; j<v.size(); j++)
                ans[v[i]-v[j]]++;
        }

        bool perfect = true, valid = true;
        vi missing;
        for(int i=1; i<=n; i++) {
            if(!ans[i]) {
                perfect = false;
                missing.push_back(i);
            }
            if(ans[i] > 1) {
                cout<<"not a ruler"<<endl;
                valid = false;
                break;
            }
        }

        if(valid) {
            if(perfect)
                cout<<"perfect"<<endl;
            else {
                cout<<"missing ";
                for(int i:missing)
                    cout<<i<<" ";
                cout<<endl;
            }
        }
    }
}