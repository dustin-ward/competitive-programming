vi squares(100);

int main() {
    for(int i=1; i<=100; ++i)
        squares[i-1] = i*i;

    int N; cin>>N;
    for(int i=0; i<N; ++i) {
        string s; cin>>s;
        
        int sq=0;
        while(squares[sq++] < sz(s));
        
        vector<vector<char>> B(sq, vector<char>(sq,'*'));

        int pos = 0;
        while(pos < sz(s)) {
            B[pos/sq][pos%sq] = s[pos];
            pos++;
        }

        for(int j=0; j<sq; ++j)
            for(int i=sq-1; i>=0; --i)
                if(B[i][j] != '*') cout<<B[i][j];
        cout<<endl;
    }
}
