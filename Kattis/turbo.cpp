struct FT {
	vector<ll> s;
	FT(int n) : s(n) {}
	void update(int pos, ll dif) { // a[pos] += dif
		for (; pos < sz(s); pos |= pos + 1) s[pos] += dif;
	}
	ll query(int pos) { // sum of values in [0, pos)
		ll res = 0;
		for (; pos > 0; pos &= pos - 1) res += s[pos-1];
		return res;
	}
	int lower_bound(ll sum) {// min pos st sum of [0, pos] >= sum
		// Returns n if no sum is >= sum, or -1 if empty sum is.
		if (sum <= 0) return -1;
		int pos = 0;
		for (int pw = 1 << 25; pw; pw >>= 1) {
			if (pos + pw <= sz(s) && s[pos + pw-1] < sum)
				pos += pw, sum -= s[pos-1];
		}
		return pos;
	}
};

int main() {
    int N; cin>>N;
    vi DAT(N);
    FT ft(N);
    int last = 0;
    for(int i=0; i<N; i++) {
        int x; cin>>x; x--;
        DAT[x] = i;
        int dis = x-i;
        ft.update(i,dis-last);
        last = dis;
    }

    for(int i=0, j=N-1, k=1; j>=0; i+=(j--)*k, k*=-1) {
        int idx = DAT[i];
        cout<<abs(ft.query(idx+1))<<endl;
        if(k>0) {
            ft.update(0,-1);
            ft.update(idx+1,1);
        } else {
            ft.update(idx+1,1);
        }
    }
}
