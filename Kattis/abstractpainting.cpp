const ll P = 1E9+7;

int main() {
    int T; cin>>T;
    while(T--) {
        int R,C; cin>>R>>C;
        ll ans;
        for(int i=0; i<R; i++) {
            for(int j=0; j<C; j++) {
                if(!i && !j) {
                    ans = 18;
                } else if(!i || !j) {
                    ans = (ans*6)%P;
                } else {
                    ans = (ans*2)%P;
                }
            }
        }
        cout<<ans<<endl;
    }
}
