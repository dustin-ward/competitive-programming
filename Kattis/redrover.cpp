int main() {
    string s; cin>>s;

    int ans = sz(s);
    for(int i=0; i<sz(s); ++i) {
        for(int j=2; j<sz(s); ++j) {
            string s2 = s.substr(i,j);
            // debug(s2);
            int c = 1;
            int p = i+j;
            int temp = s.find(s2, p);
            while(p < sz(s) && temp != -1) {
                c++;
                p = temp+j;
                temp = s.find(s2, p);
            }

            int tempAns = sz(s) - ((c-1) * j) + c;
            // debug(c);
            // debug(tempAns);
            ans = min(ans, tempAns);
        }
    }
    cout<<ans<<endl;
}
