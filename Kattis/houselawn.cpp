struct Lawnmower {
    string name;
    int p;
    ll r;
};

int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    ll L,M; cin>>L>>M;
    cin.ignore();
    vector<Lawnmower> V;
    for(int i=0; i<M; ++i) {
        Lawnmower l;
        string line;
        getline(cin,line);
        // debug(line);
        int pos = line.find(',');
        l.name = line.substr(0, pos);
        istringstream iss(line.substr(pos));
        char g;
        ll c,t,r;
        iss>>g>>l.p>>g>>c>>g>>t>>g>>r;
        l.r = 10080*c*t / (t+r);
        if(l.r >= L)
            V.push_back(l);
    }

    if(!sz(V)) {
        cout<<"no such mower"<<endl;
    }

    int minPrice = INT_MAX;
    for(auto &l:V)
        minPrice = min(minPrice, l.p);

    for(auto &l:V) {
        if(l.p == minPrice)
            cout<<l.name<<endl;
    }
}