int N,M;
vi deadend;
vector<vi> G, G_forced;

void dfs_helper(int n, vi &visited, int rand) {
	visited[n] = 1;
	if(G_forced[n].empty())
		deadend[n] = 1;
	for(int i:G_forced[n]) {
		if(!visited[i]) 
			dfs_helper(i, visited, rand);
	}
	if(!rand) {
		for(int i:G[n]) {
			if(!visited[i]) 
				dfs_helper(i, visited, 1);
		}
	}
}

void DFS(int n) {
	vi visited(N, 0);
	dfs_helper(n, visited, 0);
}

int main() {
	cin>>N>>M;
	G.resize(N);
	G_forced.resize(N);
	deadend.resize(N, 0);

	for(int i=0; i<M; ++i) {
		int u,v; cin>>u>>v;
		if(u < 0)
			G_forced[abs(u)-1].push_back(v-1);
		else
			G[u-1].push_back(v-1);
	}

	DFS(0);

	int ans = 0;
	for(int i:deadend)
		ans += i;
	cout<<ans<<endl;
}
