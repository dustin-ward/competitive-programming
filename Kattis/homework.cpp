string S, S1, S2;
bool DP[5001][5001];

bool f(int s, int s1, int s2) {
	if(s == sz(S))
		return true;
	if(s1 >= sz(S1) || s2 >= sz(S2))
		return false;
	if(S[s] != S1[s1] && S[s] != S2[s2])
		return false;
	
	bool ans = false;
	if(S[s] == S1[s1])
		ans = ans || f(s+1, s1+1, s2);

	if(S[s] == S2[s2])
		ans = ans || f(s+1, s1, s2+1);
	return ans;
}

int main() {
	for(int i=0; i<5001; ++i)
		for(int j=0; j<5001; ++j)
			DP[i][j] = -1;

	cin>>S>>S1>>S2;
	cout<<(f(0,0,0) ? "yes" : "no")<<endl;
}
