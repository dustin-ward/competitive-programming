int main() {
    string a,b;
    while(cin>>a>>b && !(a == "0" && b == "0")) {
		int ans = 0;
        int acc = 0;
        reverse(begin(a), end(a));
        reverse(begin(b), end(b));
        for(int i=0; i<max(sz(a),sz(b)); i++) {
            int x = (i<sz(a) ? a[i]-'0' : 0);
            int y = (i<sz(b) ? b[i]-'0' : 0);
            ans += acc = acc+x+y > 9;
        }

        if(ans) {
            cout<<ans<<" carry operation";
            if(ans>1) cout<<"s";
            cout<<"."<<endl;
        } else {
            cout<<"No carry operation."<<endl;
        }
	}
}
