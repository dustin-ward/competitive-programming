map<int,int> L;
int idx(int i) {
    if(!L.count(i))
        L[i] = sz(L);
    return L[i];
}

int main() {
    int N,C; cin>>N>>C;
    vi val,freq,first,ans;
    for(int i=0; i<N; i++) {
        int temp; cin>>temp;
        int j = idx(temp);
        if(j >= sz(freq)) {
            val.push_back(temp);
            freq.push_back(0);
            first.push_back(INT_MAX);
            ans.push_back(j);
        }

        freq[j]++;
        first[j] = min(first[j], i);
    }

    sort(all(ans), [&](const int &a, const int &b) {
        if(freq[a] == freq[b])
            return first[a] < first[b];
        return freq[a] > freq[b];
    });

    for(int &i:ans) {
        for(int j=0; j<freq[i]; j++)
            cout<<val[i]<<" ";
    }
    cout<<endl;
}
