const ll mod = 1E9+7;
struct Mod {
	ll x;
	Mod(ll xx) : x(xx) {}
	Mod operator+(Mod b) { return Mod((x + b.x) % mod); }
	Mod operator-(Mod b) { return Mod((x - b.x + mod) % mod); }
	Mod operator*(Mod b) { return Mod((x * b.x) % mod); }
	Mod operator^(ll e) {
		if (!e) return Mod(1);
		Mod r = *this ^ (e / 2); r = r * r;
		return e&1 ? *this * r : r;
	}
};

int main() {
    int L,R; cin>>L>>R;
    Mod ans(0);

    for(int N=L; N<=R; N++) {
        Mod t1(5);
        t1 = t1^((N+1)/2);
        Mod t2(2);
        t2 = t2^((N+1)/2);
        ans = ans + t1 + t2;
    }

    cout<<ans.x<<endl;
}
