int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    int N,M; cin>>N>>M;
    vi W(N);
    for(auto &i:W)
        cin>>i;
    sort(W.rbegin(), W.rend());

    vpii F(M);
    for(auto &p:F)
        cin>>p.second>>p.first;
    sort(F.rbegin(), F.rend());

    int w=0,f=0;
    ll ans = 0;
    while(N && M) {
        ans += (ll)F[f].first * (ll)W[w];
        N--;
        w++;
        F[f].second--;
        if(!F[f].second)
            f++,M--;
    }
    cout<<ans<<endl;
}