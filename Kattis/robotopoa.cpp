int main() {
    int N; cin>>N;
    while(N--) {
        int l1,a1,l2,a2,lt,at;
        cin>>l1>>a1>>l2>>a2>>lt>>at;

        int R1,R2;
        int ans = 0;
        int maxR1 = lt/l1;
        for(int numR1=1; numR1<=maxR1; ++numR1) {
            int numR2 = (lt-(numR1*l1))/l2;
            if(!numR2) continue;
            if((numR1*l1+numR2*l2 == lt) && (numR1*a1+numR2*a2 == at)) {
                ans++;
                R1 = numR1;
                R2 = numR2;
            }
        }
        if(ans == 1)
            cout<<R1<<" "<<R2<<endl;
        else
            cout<<"?"<<endl;
    }
}
