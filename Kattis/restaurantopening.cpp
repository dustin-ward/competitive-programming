int main() {
    int N,M; cin>>N>>M;

    vector<vi> B(N, vi(M));
    for(int i=0; i<N; i++)
        for(int j=0; j<M; j++)
            cin>>B[i][j];

    vector<vi> ans(N, vi(M));
    for(int i=0; i<N; i++) {
        for(int j=0; j<M; j++) {
            for(int i2=0; i2<N; i2++) {
                for(int j2=0; j2<M; j2++) {
                    int dis = abs(i-i2)+abs(j-j2);
                    ans[i][j] += dis * B[i2][j2];
                }
            }
        }
    }

    debug(ans);
    int m = INT_MAX;
    for(int i=0; i<N; i++)
        for(int j=0; j<M; j++)
            m = min(m, ans[i][j]);
    cout<<m<<endl;
}
