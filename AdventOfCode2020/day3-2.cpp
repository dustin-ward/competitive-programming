int N,M;
vector<string> B;

int f(int dy, int dx) {
    int y=0,x=0;
    int ans = 0;
    while(y < N) {
        if(B[y][x] == '#')
            ans++;
        y += dy;
        x = (x+dx) % M;
    }
    return ans;
}

int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    string line;
    while(getline(cin, line))
        B.push_back(line);
    N = B.size();
    M = B[0].length();
    
    ll ans = 1;
    ans *= f(1, 1);
    ans *= f(1, 3);
    ans *= f(1, 5);
    ans *= f(1, 7);
    ans *= f(2, 1);
    
    cout<<ans<<endl;
}