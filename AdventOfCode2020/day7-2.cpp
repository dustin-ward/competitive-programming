map<string, vector<pair<int,string>>> M;
map<string, int> B;

ll f(string s) {
    if(B.find(s) != B.end())
        return B[s];
    if(M.find(s) == M.end())
        return B[s] = 1;

    ll ans = 1;
    for(auto p:M[s])
        ans += p.first * f(p.second);
    return B[s] = ans;
}

