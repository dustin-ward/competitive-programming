bool seats[1024];

int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    string line;
    while(getline(cin, line)) {
        string rowS = line.substr(0,7);
        string colS = line.substr(7);

        int hi = 127, lo = 0;
        for(int i=0; i<7; i++) {
            int mid = lo + (hi-lo)/2;
            if(rowS[i] == 'F') {
                hi = mid;
            }
            else {
                lo = mid;
            }
        }
        int row = hi;

        hi = 7, lo = 0;
        for(int i=0; i<3; i++) {
            int mid = lo + (hi-lo)/2;
            if(colS[i] == 'L') {
                hi = mid;
            }
            else {
                lo = mid;
            }
        }
        int col = hi;
        
        int id = (row*8)+col;
        seats[id] = 1;
    }
    
    int pos = 0;
    while(!seats[pos])
        pos++;

    while(seats[pos])
        pos++;

    cout<<pos<<endl;
}